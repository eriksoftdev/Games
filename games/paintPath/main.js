function setupCanvas(canvasId, svgData) {
  var canvas = document.getElementById(canvasId);
  var ctx = canvas.getContext('2d');

  var img = new Image();
  img.onload = function() {
    ctx.drawImage(img, 0, 0);
  };
  img.src = 'data:image/svg+xml;base64,' + btoa(svgData);


  var isDrawing = false;
  var brushSize = 10;
  var brushColor = 'blue';
  ctx.lineWidth = brushSize;
  ctx.strokeStyle = brushColor;

  function getCanvasRelativeCoords(event) {
    var canvasRect = canvas.getBoundingClientRect();
    var scaleX = canvas.width / canvasRect.width;
    var scaleY = canvas.height / canvasRect.height;
    return {
      x: (event.clientX - canvasRect.left) * scaleX,
      y: (event.clientY - canvasRect.top) * scaleY
    };
  }

  canvas.addEventListener('mousedown', function(event) {
    isDrawing = true;
    var coords = getCanvasRelativeCoords(event);
    ctx.beginPath();
    ctx.moveTo(coords.x, coords.y);
  });

  canvas.addEventListener('mousemove', function(event) {
    if (isDrawing) {
      var coords = getCanvasRelativeCoords(event);
      ctx.lineTo(coords.x, coords.y);
      ctx.stroke();
    }
  });

  canvas.addEventListener('mouseup', function() {
    isDrawing = false;
  });

  function handleCanvasResize() {
    var img = new Image();
    img.onload = function() {
      ctx.clearRect(0, 0, canvas.width, canvas.height);
      ctx.drawImage(img, 0, 0, canvas.width, canvas.height);
    };
    img.src = 'data:image/svg+xml;base64,' + btoa(svgData);
  }

  window.addEventListener('resize', handleCanvasResize);
}





